# Control flow :  if (condition): - elif (condition): - else

boolean = False
if boolean:
    print("It's true!");
else:
    print("It's False!")


loc = "Bank"
if loc == "Yes":
    print("Yes")
elif loc == "BOI":
    print("BOI")
else:
    print("Bank")

